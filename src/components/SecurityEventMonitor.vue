    ElMessage.success("‰∫ã‰ª∂Â∑≤Ê†áËÆ∞‰∏∫Â∑≤Ëß£ÂÜ≥");
  } catch (error) {
    if (error !== "cancel") {
      ElMessage.error("Êìç‰ΩúÂ§±Ë¥•");
    }
  }
};

const viewEventDetails = (event: any) => {
  selectedEvent.value = event;
  showEventDetails.value = true;
};

const handleSizeChange = (size: number) => {
  pageSize.value = size;
  currentPage.value = 1;
};

const handleCurrentChange = (page: number) => {
  currentPage.value = page;
};

const formatDateTime = (timestamp: string) => {
  return new Date(timestamp).toLocaleString("zh-CN");
};

const getEventTypeColor = (type: string) => {
  const colorMap = {
    encryption: "primary",
    backup: "success",
    recovery: "warning",
    access: "info",
    error: "danger",
    system: "info",
  };
  return colorMap[type] || "info";
};

const getEventTypeText = (type: string) => {
  const textMap = {
    encryption: "Âä†ÂØÜ",
    backup: "Â§á‰ªΩ",
    recovery: "ÊÅ¢Â§ç",
    access: "ËÆøÈóÆ",
    error: "ÈîôËØØ",
    system: "Á≥ªÁªü",
  };
  return textMap[type] || type;
};

const getSeverityColor = (severity: string) => {
  const colorMap = {
    low: "success",
    medium: "warning",
    high: "danger",
    critical: "danger",
  };
  return colorMap[severity] || "info";
};

const getSeverityText = (severity: string) => {
  const textMap = {
    low: "‰Ωé",
    medium: "‰∏≠",
    high: "È´ò",
    critical: "‰∏•Èáç",
  };
  return textMap[severity] || severity;
};

const generateCSV = (events: any[]) => {
  const headers = ["Êó∂Èó¥", "Á±ªÂûã", "‰∏•ÈáçÁ®ãÂ∫¶", "ÊèèËø∞", "Áî®Êà∑", "IP", "Áä∂ÊÄÅ"];
  const rows = events.map((event) => [
    formatDateTime(event.timestamp),
    getEventTypeText(event.type),
    getSeverityText(event.severity),
    event.description,
    event.user || "",
    event.ip || "",
    event.resolved ? "Â∑≤Ëß£ÂÜ≥" : "Êú™Ëß£ÂÜ≥",
  ]);

  return [headers, ...rows]
    .map((row) => row.map((cell) => `"${cell}"`).join(","))
    .join("\n");
};

const downloadCSV = (content: string, filename: string) => {
  const blob = new Blob(["\ufeff" + content], {
    type: "text/csv;charset=utf-8;",
  });
  const link = document.createElement("a");
  link.href = URL.createObjectURL(blob);
  link.download = filename;
  link.click();
};

onMounted(() => {
